  <h2 class="title">Clavier numérique 4×4</h2> 
   <div style="font-size: 180%;" class="44" >
   
    <p style="text-align: left;" class="tt">
        Clavier numérique est un ensemble de 16 boutons qui montés sous forme de matrice, c’est à dire que tous les boutons d’une colonne sont reliés une entrée et tous les boutons d’une même ligne sont reliés à une autre. Lorsqu’on appuie sur un bouton l’entrée correspondant à la ligne est reliée à l’entrée correspondant à la colonne ce qui ferme le circuit. L’avantage de ce type de montage est que l’on peut gérer 16 boutons avec seulement 8 entrées du microcontrôleur.</p>
         

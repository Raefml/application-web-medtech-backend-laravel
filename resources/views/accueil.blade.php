       <div style="font-size: 180%;" class="44" >
       
        <p style="text-align: left;" class="tt">
            Le PIC est une famille de microcontrôleurs, qui est fabriqué par les différentes sociétés telles que NXP, microchip, etc. Le PIC signifie «contrôleur d'interface périphérique», qui contient des mémoires, minuteries / compteurs , communication série, interruptions et convertisseurs ADC intégrés dans une seule puce intégrée.</p>
             

  <h2 class="title">Pourquoi construire un « un micro-contrôleur » ,</h2> 
   <div style="font-size: 180%;" class="44" >
   
    <p style="text-align: left;" class="tt">
        Les tendances récentes de la technologie de pointe sont utiles pour développer les gadgets électroniques les plus avancés. La plupart de ces appareils électroniques sont développés à l'aide de microcontrôleurs. Le microcontrôleur est un composant électronique, qui est programmé pour effectuer diverses opérations de contrôle. Il existe différents types de microcontrôleurs disponibles, tels que Microcontrôleurs 8051, AVR, ARM et PIC , etc., qui sont programmés à l'aide des outils de développement intégrés.</p>
       
